package com.beanloaf.objects;

public class TextPropertyObject {

    public final String title;
    public final String tag;
    public final String date;
    public final String body;

    public TextPropertyObject(final String title, final String tag, final String date, final String body) {
        this.title = title;
        this.tag = tag;
        this.date = date;
        this.body = body;
    }

}
